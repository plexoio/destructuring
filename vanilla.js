// Vanilla JS Array

let ages = [30, 26, 27];
let john = ages[0];
let mary = ages[1];
let joe = ages[2];

console.log(`John: ${john}\nMary: ${mary}\nJoe: ${joe}`);
console.log('-------------');

// Vanilla JS Object

let jobs = {
    mike: "designer",
    jill: "developer",
    alicia: "accountant"
};

let mike = jobs.mike;
let jill = jobs.jill;
let alicia = jobs.alicia;

console.log(`Mike: ${mike}\nJill: ${jill}\nAlicia: ${alicia}`);
console.log('-------------');
// Vanilla JS Subsets

let languages = ['English', 'French', 'Spanish', 'German', 'Japanese'];

let johnNative = languages[0];
let johnSecundary = languages[1];

console.log(`John First Language: ${johnNative}\nJohn Second Language: ${johnSecundary}`);
console.log('-------------');